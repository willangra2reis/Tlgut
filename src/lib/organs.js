export const ORGAN_ZONES = Object.entries(ORGAN_POINTS).flatMap(([id, pts]) =>
  pts.map(([cx, cy]) => ({ id, label: ORGAN_LABELS[id], cx, cy })),
);

export const ORGAN_LIST = Object.keys(ORGAN_LABELS).map((id) => ({ id, label: ORGAN_LABELS[id] }));

export const ORGAN_CENTROIDES = Object.entries(ORGAN_POINTS).map(([id, pts]) => ({
  id,
  label: ORGAN_LABELS[id],
  cx: pts.reduce((s, [x]) => s + x, 0) / pts.length,
  cy: pts.reduce((s, [, y]) => s + y, 0) / pts.length,
}));

export function nearestOrgan(px, py) {
  let best = null, bestDist = Infinity;
  ORGAN_ZONES.forEach((z) => {
    const dx = px - z.cx, dy = py - z.cy;
    const d = Math.sqrt(dx * dx + dy * dy);
    if (d < bestDist) { bestDist = d; best = z; }
  });
  return best;
}